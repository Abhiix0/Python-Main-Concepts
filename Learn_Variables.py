#  Learning Python: [Variables]

# Example 1: Storing your age in a variable
age = 20  # Here, 'age' is a variable that stores the number 20
print("My age is:", age)

# Example 2: Changing the value of a variable
age = 21  # Now we update 'age' to a new value
print("Now my age is:", age)

# Example 3: Using variables to store text
name = "Alice"  # 'name' stores a string (text)
print("Hello, my name is", name)

# Example 4: Combining variables in a sentence
city = "New York"
print(name, "lives in", city)

# Example 5: Doing math with variables
apples = 5
oranges = 3
total_fruit = apples + oranges  # Add the two variables together
print("Total fruit:", total_fruit)

# Example 6: Swapping variable values
a = 10
b = 20
print("Before swap: a =", a, ", b =", b)
# Swap values
temp = a
a = b
b = temp
print("After swap: a =", a, ", b =", b)

# Example 7: Variables can change type
score = 100  # score is a number
print("Score:", score)
score = "One hundred"  # Now score is a string
print("Score (as text):", score)